def get_industry_group(industry_str):
    """Map a specific industry string to one of ~10 high-level groups."""
    if not industry_str:
        return "Other"
    
    # Check the first word/prefix of the industry
    for key, group in INDUSTRY_GROUP_MAP.items():
        if industry_str.startswith(key) or key.lower() in industry_str.lower():
            return group
    
    # Fallback
    return "Other"


def classify_company(company_name):
    """Classify a company into an industry."""
    if not company_name or company_name.strip() == '':
        return "Unknown"
    
    name = company_name.strip()
    
    # 1. Check known companies dict
    if name in KNOWN_COMPANIES:
        return KNOWN_COMPANIES[name]
    
    # 2. Try case-insensitive match
    name_lower = name.lower()
    for known, industry in KNOWN_COMPANIES.items():
        if known.lower() == name_lower:
            return industry
    
    # 3. Handle encoding-garbled names (common patterns)
    garbled_mappings = {
        "hler": "Manufacturing - Food Processing Equipment",  # Bühler variants
        "L'Or": "Manufacturing - Beauty & Cosmetics",  # L'Oréal
        "Lowe": "Retail - Home Improvement",  # Lowe's
        "rieux Nutrisciences": "Professional Services - Food Safety Testing",  # Mérieux
        "Schwan": "Manufacturing - Frozen Food",  # Schwan's
        "Warner": "Retail - Appliances",  # Warner's Stellian
        "Saint Paul's Outreach": "Nonprofit - Religious",
        "Saint Paul\u2019s Outreach": "Nonprofit - Religious",
        "Face Foundri": "Retail - Skincare",
        "Ed\u2019s Buckthorn": "Professional Services - Landscaping",
    }
    for fragment, industry in garbled_mappings.items():
        if fragment in name:
            return industry
    
    # 4. Use keyword heuristics
    for pattern, industry in KEYWORD_PATTERNS:
        if re.search(pattern, name, re.IGNORECASE):
            return industry
    
    return "Unknown - Needs Research"


