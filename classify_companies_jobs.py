def describe_job_title(title):
    """Generate a description for a job title."""
    if not title:
        return ""
    
    t = title.strip()
    
    # Check exact matches first
    if t in JOB_TITLE_DESCRIPTIONS:
        return JOB_TITLE_DESCRIPTIONS[t]
    
    # Case-insensitive exact match
    for known, desc in JOB_TITLE_DESCRIPTIONS.items():
        if known.lower() == t.lower():
            return desc
    
    # Keyword-based description
    descriptions = []
    for pattern, desc in JOB_KEYWORD_DESCRIPTIONS:
        if re.search(pattern, t, re.IGNORECASE):
            descriptions.append(desc)
    
    if descriptions:
        return f"A professional {descriptions[0]}."
    
    return "A specialized professional role. (Description not available - requires further research.)"


